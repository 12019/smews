int main(){

	uint8_t buf[128];
	int16_t size;
	uint8_t i;
	HARDWARE_INIT;

	//stack_top = (char*)&buf[127];

	while(1)

		if(!DEV_DATA_TO_READ) {
			DEV_WAIT;
			continue;
		}
		else {
			connect_socket();
			if(!tls_handshake()) return 0;
			else break;

		}

	/* printf("Stack usage : %d", stack_top - stack_bottom ); */
	uint8_t get_recv = 0;
	/* receiving app data */
	while(1){
		if(net_receive() > 0){
			if(tls_get_app_data()){
				if(!get_recv){
					copy_bytes(http_ok,record_buffer + START_BUFFER,0,373);
					tls_send_app_data(373);
					get_recv = 1;
				}
			}
			else break;
		}


		if( (size = user_input(record_buffer + START_BUFFER)) > 0){
			tls_send_app_data(size);

			for(i = 0; i < size; i++)
				buf[i] = 0;
		}

	}

	return 1;
}


